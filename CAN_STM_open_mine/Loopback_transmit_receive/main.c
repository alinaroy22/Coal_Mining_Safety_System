void can_receive(void)
{
	char msg[50];
	CAN_RxHeaderTypeDef RxHeader;
	uint8_t RxData[5];

	//waiting for message to come
	while(!(HAL_CAN_GetRxFifoFillLevel(&hcan1, CAN_RX_FIFO0)));

	if(HAL_CAN_GetRxMessage(&hcan1, CAN_RX_FIFO0, &RxHeader, RxData) != HAL_OK)
	{
		Error_Handler();
	}

	HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);

	sprintf(msg,"Message Received: %s\r\n", RxData);
	HAL_UART_Transmit(&huart5, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);
}

void can_transmit(void)
{
	char msg[50];
	uint8_t TxData[] = {'H', 'E', 'L', 'L', 'O'};
	CAN_TxHeaderTypeDef TxHeader;
	uint32_t TxMailbox;

	TxHeader.DLC = 5;       //send "Hello". so length is 5;
	TxHeader.StdId = 0x543;   //any id can be choosen
	TxHeader.IDE = CAN_ID_STD;
	TxHeader.RTR = CAN_RTR_DATA;

	if (HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailbox) != HAL_OK)
	{
		Error_Handler();
	}

	while (HAL_CAN_IsTxMessagePending(&hcan1, TxMailbox));

	HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12);

	sprintf(msg,"Message Transmitted: %s\r\n", TxData);
	HAL_UART_Transmit(&huart5, (uint8_t *)msg, strlen(msg), HAL_MAX_DELAY);

}
